
class SimpleMath {
  /**
   * This function adds two numbers together.
   * @param {x} input any number
   * @param {y} input any number
   * @returns {number} that addition of the two numbers
   */
  static add (x, y) {
    return x + y
  }

  /**
   * This function subtracts on number from the other
   * @param {x} input any number
   * @param {y} input any number
   * @returns {result} of subtraction of two numbers
  */
  static subtract (x, y) {
    return x - y
  }
}

module.exports = SimpleMath
